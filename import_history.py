    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()

    # Проверим сколько уже есть
    c.execute("SELECT COUNT(*) FROM transactions WHERE user_id=?", (USER_ID,))
    existing = c.fetchone()[0]
    print(f"Уже в базе: {existing} записей")

    # Очистим старые тестовые данные если были
    c.execute("DELETE FROM transactions WHERE user_id=?", (USER_ID,))
    print("Старые записи удалены")

    # Вставляем все транзакции
    inserted = 0
    for date, amount, category, description, tx_type in transactions:
        c.execute(
            "INSERT INTO transactions (user_id,date,amount,category,description,type) VALUES (?,?,?,?,?,?)",
            (USER_ID, date, amount, category, description, tx_type)
        )
        inserted += 1

    conn.commit()
    conn.close()

    income = sum(t[1] for t in transactions if t[4] == "income")
    expense = sum(t[1] for t in transactions if t[4] == "expense")
    print(f"Загружено: {inserted} записей")
    print(f"Доходы: {income:,} ₽")
    print(f"Расходы: {expense:,} ₽")
    print(f"Баланс: {income - expense:,} ₽")
